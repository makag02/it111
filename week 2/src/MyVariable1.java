public class MyVariable1 {
    //program practice for declaring variables
    public static void main (String[] args){

        String name = "Maka";  //string variable with my name Maka
        int age = 28; //int variable with a value of 28

        System.out.println("My String variable \"name\" is "+name);
        System.out.println("My int variable \"age\" is "+age);

        //declaring various string variables
        String building, tree, lake, ocean;
        building = "T-Mobile Park";
        tree = "Tamarack";
        lake = "Whitestone Lake";
        ocean = "Pacific Ocean";

        //declaring various int variables
        int weeks, decades, months, years;
        weeks = 4; decades = 10; months = 3; years = 5;

        //print variables to the console
        System.out.println(building);
        System.out.println(tree);
        System.out.println(lake);
        System.out.println(ocean);
        System.out.println(weeks);
        System.out.println(decades);
        System.out.println(months);
        System.out.println(years);
    } //end main
}
